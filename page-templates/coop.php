  <h2>Son histoire</h2>
  <p>
    Avec ses bâtiments industriels, dont plus de 40 000 m² sont voués à la réhabilitation, et sa localisation au coeur du port industriel de Strasbourg, à deux pas du Rhin et de l’Allemagne, le site de la COOP a une identité forte et singulière.
    Site de 9 hectares, la « COOP » est principalement constituée des emprises et bâtiments de l’ancienne « union de coopérateurs d’Alsace », réseau de distribution emblématique du territoire.
    La « coopé » y installa son siège en 1911 et y développa ses activités de production et distribution au cours du XXème siècle. Les difficultés de l’entreprise entrainèrent à partir des années 2010 une désaffectation progressive du site, jusqu’à la cessation d’activité.
  </p>

  <h2>Sa rénovation</h2>
  <p>
    L’organisation du festival d’arts numériques et de musiques électroniques Ososphère, les occupations temporaires d’artistes, les activités logistiques et portuaires du site, la proximité du quartier du Port du Rhin, ou l’esprit coopératif de COOP d’Alsace ontconforté les premières orientations du projet : un lieu de nouvelles mixités entre économie (portuaire, industrielle, créative, numérique, sociale & solidaire, …), culture et vie citoyenne. L’état et l’histoire des lieux éclairent ainsi le devenir de la coop. Coop Alsace fut un projet entrepreneurial et social, le projet d’une autre manière de consommer, un projet de valorisation de savoir-faire et produits locaux.

    La « coop » vise à porter et décliner cet héritage dans ses projets et sa démarche. Les fonctions historiques orientent les projets de réaffectation des différents bâtiments. La coop devient le lieu où peut s’inventer avec l’architecte-urbaniste-paysagiste Alexandre Chemetoff une « culture coopérative de la ville »: l’innovation et les projets s’inscrivent dans une approche citoyenne et collaborative ; les cultures, savoirs, idées et savoir-faire s’échangent ; des projets économiques et solidaires, à la fois ouverts sur l’Europe et en prise avec le territoire du Port du Rhin, y trouvent ancrage ; un « réseau coop » transfrontalier se structure progressivement, à l’image de l’ancien réseau de supérettes « Coop ».
  </p>
